

const percentage = 76

if (percentage >= 90) {
    console.log("A+");
} else if (percentage >= 80) {
    console.log("A1");
} else if (percentage >= 70) {
    console.log("A");
} else if (percentage >= 60) {
    console.log("B");
} else if (percentage >= 50) {
    console.log("C");
}
else if (percentage >= 0 && percentage < 50) {
    console.log("FAIL");
}
else {
    console.log("Invalid Percentage");
}



























